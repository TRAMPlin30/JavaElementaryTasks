package com.ElementaryTasks.Envelope;

public class Envelopes {

    private double width;
    private double height;

    public Envelopes(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
